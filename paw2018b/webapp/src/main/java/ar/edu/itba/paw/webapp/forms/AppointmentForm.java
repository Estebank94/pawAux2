package ar.edu.itba.paw.webapp.forms;

public class AppointmentForm {

    String day;
    String time;

    public AppointmentForm() {

    }
    public String getDay() {
        return day;
    }

    public void setDay(String day) {
        this.day = day;
    }

    public String getTime() {
        return time;
    }

    public void setTime(String time) {
        this.time = time;
    }
}
